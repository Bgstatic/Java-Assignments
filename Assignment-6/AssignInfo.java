

public class AssignInfo {

	private String assignResult;
	private String assignMessage;
	private Course course;
	

	// getter and setter methods
	public String getAssignResult() {

		return assignResult;

	}

	public String getAssignMessage() {

		return assignMessage;
	}

	public Course getCourse() {

		return course;
	}

	public void setAssignResult(String newAssignResult) {

		assignResult = newAssignResult;
	}

	public void setAssignMessage(String newAssaignMessage) {

		assignMessage = newAssaignMessage;

	}

	public void setCourse(Course newCourse) {

		course = newCourse;
	}

}